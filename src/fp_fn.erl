%%%_* Module declaration ======================================================
-module(fp_fn).

-behaviour(fp_functor).

%%%_* Exports =================================================================
-export([fmap/2]).

%%%_* Includes ================================================================
-include("include/fp_macros.hrl").
-include("include/fp_types.hrl").

%%%_* Macros ==================================================================

%%%_* Code ====================================================================
-spec fmap(fn(B, C), fn(A, B)) -> fn(A, C).
fmap(F, Fun) when ?isFn(F), ?isFn(Fun) -> fun(Arg) -> F(Fun(Arg)) end.

